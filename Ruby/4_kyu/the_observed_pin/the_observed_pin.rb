ADJACENTS = {
  '1' => ['1', '2', '4'],
  '2' => ['1', '2', '3', '5'],
  '3' => ['2', '3', '6'],
  '4' => ['1', '4', '5', '7'],
  '5' => ['2', '4', '5', '6', '8'],
  '6' => ['3', '5', '6', '9'],
  '7' => ['4', '7', '8'],
  '8' => ['5', '7', '8', '9', '0'],
  '9' => ['6', '8', '9'],
  '0' => ['8', '0']
}

def get_pins(observed)
  return ADJACENTS[observed] if observed.size == 1
  result = []
  get_pins(observed[1..-1]).each do |chr|
    ADJACENTS[observed[0]].each do |d|
      result << d + chr
    end
  end
  result
end

