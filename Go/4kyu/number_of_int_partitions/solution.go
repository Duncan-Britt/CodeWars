func expand(s, m int) []int {
    nums := make([]int, 0)

    n := s
    x := s
    for true {
        n = x - m
        if n < m {
            break
        }
        x = n
    }

    if x < m {
        nums = append(nums, n, x)
    } else {
        nums = append(nums, append([]int{n}, expand(x, m)...)...)
    }

    return nums
}

